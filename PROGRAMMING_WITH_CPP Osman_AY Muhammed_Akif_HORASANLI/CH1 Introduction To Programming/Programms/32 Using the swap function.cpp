#include<iostream>
using namespace std;

int main()
{
	// given integers
	int a = 5, b = 3;

	// show values
	cout<<"Before swapping: a = "<<a<<" and b = "<<b<<endl;
	
	// use the swap() function
	swap(a,b);

	// show values
	cout<<"After swapping:  a = "<<a<<" and b = "<<b<<endl;

	system("PAUSE");
	return 0;
}
